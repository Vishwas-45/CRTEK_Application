#include <iostream>
using namespace std;

int main()
{
    float a, b;
    cout << "Simple Calculator" << endl;
    cout << "Enter A and B values" << endl;
    cin >> a >> b;
    cout << "Addition is " << a + b << endl;
    cout << "Substraction is " << a - b << endl;
    cout << "Multiplication is " << a * b << endl;
    cout << "Division is " << a / b << endl;
    return 0;
}